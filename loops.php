<h3>PHP Loops</h3>
Often when you write code, you want the same block of code to run over and over again 
a certain number of times. So, instead of adding several almost equal code-lines in a 
script, we can use loops.

Loops are used to execute the same block of code again and again, as long as a certain 
condition is true.

In PHP, we have the following loop types:

 • <a target=_blank href="while_loop.php">while</a> - loops through a block of code as long as the specified condition is true
 • <a target=_blank href="do_while_loop.php">do...while</a> - loops through a block of code once, and then repeats the loop as long 
   as the specified condition is true
 • <a target=_blank href="for.php">for</a> - loops through a block of code a specified number of times
 • <a target=_blank href="foreach.php">foreach</a> - loops through a block of code for each element in an array
 
 The following chapters will explain and give examples of each loop type.

